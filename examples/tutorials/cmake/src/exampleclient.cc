#include <iostream>
#include "libplayerc++/playerc++.h"

using namespace PlayerCc;

int main(int argc, char *argv[])
{
  PlayerClient robot("localhost", 6665);
  Position2dProxy pp(&robot, 0);

  while(!pp.IsValid())
    robot.Read();

  for (int i=0; i < 10; i++)
  {
    robot.Read();
    std::cout << pp << std::endl;
  }

  return 0;
}

